        <h4>Técnicas del cepillado</h4>
        <br>
        <ul>
          <li>Cepilla los dientes anteriores colocando las cerdas sobre la encía en un ángulo de 45 grados. Las cerdas deben estar en contacto con la superficie dental y la encía</li>
          <li>Cepilla suavemente la superficie dental externa de 2 o 3 dientes por vez con un movimiento rotatorio de adelante hacia atrás. Mueve el cepillo al próximo grupo de 2 o 3 dientes y repite la operación.</li>
          <img src="imagenes/cepillado.jpg">
          <li>Mantén un ángulo de 45 grados con las fibras en contacto con la superficie dental y la encía. Para el cepillado de los molares, cepilla suavemente con un movimiento circular al mismo tiempo que realizas un movimiento de adelante hacia atrás sobre toda la superficie interna.</li>
          <li>Inclina el cepillo verticalmente detrás de los dientes frontales. Haz varios movimientos de arriba hacia abajo usando la parte delantera del cepillo.</li>
          <img src="imagenes/cepillado2.jpg">
          <li>Pon el cepillo sobre la superficie masticatoria y haz un movimiento suave de adelante hacia atrás. Cepilla la lengua de adelante hacia atrás para eliminar las bacterias que producen el mal aliento.</li> 
        </ul>
