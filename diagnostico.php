      <h2>Técnicas de higiene oral</h2>
      <ul>
        <li>Cepilla tus dientes al menos <strong>dos veces al día</strong> para prevenir caries, procura no dejar pasar mas de 15 minutos entre el final de cada comida y el cepillado.</li>
        <li>Ten una buena técnica de cepillado, no olvides cepillar todas las caras de los dientes, exterior, interior y zonas de masticación</li>
        <li>Elige un buen cepillo de dientes, te recomendamos que sea un cepillo de cerdas suaves, para que puedas realizar una correcta limpieza penetrando en los espacios y además no arañe el diente y cuide las encías.</li>
        <li>Cambia cada 3 meses de cepillo, de lo contrario perderás eficacia en el cepillado.</li>
        <li>No te olvides de limpiar la lengua: cepilla la lengua con el propio cepillo o con un limpiador lingual.</li>
        <li>Usa seda dental: utiliza la seda dental diariamente para una higiene dental completa, ya que ayuda a eliminar la placa y las partículas de comida en lugares que el cepillo dental no puede alcanzar fácilmente.</li>
        <li>Enjuágate con un colutorio dental: es el complemento ideal para terminar con la higiene dental. Los enjuagues bucales ayudan a suprimir temporalmente el mal aliento, reducen las bacterias en la boca y la refrescan, dejando en ella un sabor agradable.</li>
        <li>Acude al menos una vez al año al dentista: sométete a revisiones dentales periódicas (cada 6 meses) o por lo menos una vez al año. La atención preventiva ayuda a evitar que ocurran problemas.</li>
      </ul>
