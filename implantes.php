      <h3>Implante dental</h3>
      <p>La cirugía de implantes dentales es un procedimiento que reemplaza las raíces de los dientes con pernos metálicos que parecen tornillos y reemplaza el diente faltante, o dañado, con un diente artificial que tiene el mismo aspecto y que cumple la misma función que los dientes reales. La cirugía de implantes dentales puede ofrecer una alternativa bien aceptada a las dentaduras o a los puentes que no se fijan bien, y puede ofrecer una opción cuando una falta de raíces de los dientes naturales no permite la creación de dentaduras o reemplazos de dientes con puentes.</p>
      <p>El método seguido para una cirugía de implante dental depende del tipo de implante y del estado de la mandíbula. La cirugía de implante dental puede abarcar varios procedimientos. El principal beneficio de los implantes es un soporte sólido para todos dientes nuevos, un proceso que requiere que el hueso se cicatrice fuertemente alrededor del implante. Debido a que esta cicatrización ósea lleva tiempo, el proceso puede llevar varios meses.</p>
