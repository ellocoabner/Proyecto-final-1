      <h2></h2>
      <h3>¿Qué es la caries dental?</h3>
      <p>La caries dental es la destrucción de los tejidos de los dientes causada por la presencia de ácidos producidos por las bacterias de la placa depositada en las superficies dentales. Este deterioro de los dientes está muy influenciado por el estilo de vida, es decir influye lo que comemos, la forma cómo cuidamos nuestros dientes (nuestros hábitos de higiene), la presencia de flúor en la sal y la pasta de dientes que utilizamos. La herencia también juega un papel importante en la susceptibilidad de sus dientes a las caries.</p>
      
