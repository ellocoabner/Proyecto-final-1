            <h4>Tratamiento para la caries</h4>
            <p>Depende de la fase en la que se encuentre. Cuanto más profunda sea la caries, más minucioso deberá ser el tratamiento a realizar por el odontólogo, por eso lo más importante es la detección temprana de caries:</p>
            <div class="col" style="background-color: #E5F5F2;">
                <span class="bi bi-1-circle-fill"></span>
                En una caries inicial, el odontólogo realizará un empaste, eliminando primeramente el tejido afectado y posteriormente colocando en ese espacio el empaste.
            </div>
            <div class="col mt-2" style="background-color: #E5F5F2;">
                <span class="bi bi-2-circle-fill"></span>
                Si la caries ha alcanzado la pulpa dental, el odontólogo tendrá que realizar una endodoncia, retirando el nervio.
            </div>
            <div class="col mt-2 my-2" style="background-color: #E5F5F2;">
                <span class="bi bi-3-circle-fill" ></span>
                En los casos más avanzados, puede destruir completamente el diente, o la infección puede estar tan diseminada que será imposible salvar la pieza. En este caso es necesario hacer la extracción y reponer el diente perdido.
