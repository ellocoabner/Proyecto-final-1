              <h4><strong>Si bien la caries suele ser más común en niños, los adultos también corren riesgo de padecerla. Los tipos de caries incluyen:</strong></h4>
              <ul>
                  <li>Caries de corona: Son las más comunes, se presentan tanto en niños como en adultos, y generalemente sobre las superficies de masticación o entre los dientes.</li>
                  <li>Caries radicular: a medida que avanzamos en edad, las encías se retraen, dejando expuestas partes de la raíz del diente. Como las raíces no están recubiertas por esmalte, estas zonas expuestas pueden afectarse fácilmente.</li>
                  <li>Caries recurrentes: se pueden formar alrededor de las obturaciones y coronas existentes. Sucede debido a que dichas zonas tienen tendencia a acumular placa, lo cual finalmente produce la formación de caries.</li>
              </ul>
              <p>Los adultos corren especial riesgo de padecer caries si sufren de sequedad bucal, que es un trastorno provocado por la falta de saliva debido a algunas enfermedades, el uso de algunos medicamentos, y a tratamientos de radioterapia y quimioterapia. La sequedad bucal puede ser temporal o permanente, según su origen.</p>
              <p>Las caries pueden ser graves ya que si no se tratan, pueden destruir el diente y matar los delicados nervios de su interior, lo cual puede provocar un absceso (una infección en el extremo de la raíz). Una vez formado un absceso, el único tratamiento posible es la endodoncia (también llamada tratamiento del conducto radicular), una cirugía o la extracción del diente.</p>
