      <h2>Profilaxis</h2>
      <h3>¿Qué es la Profilaxis</h3>
      <p>Es un procedimiento práctico en el cual el objetivo principal es prevenir patologías periodontales y limpiar la superficie de los dientes eliminando el sarro y la placa endurecida que se encuentra adherida y acumulada en ciertas zonas, como los espacios interdentales y las líneas de las encías, que son inaccesibles para el cepillo.</p>

      <p>La limpieza bucal profesional o profilaxis dental es una práctica esencial para que tus dientes permanezcan totalmente sanos y limpios.</p>
